#!/usr/bin/env node
/**
 * Generates 3 sharp starter questions per topic by asking Claude to skim each
 * deliverable / source and propose questions a busy founder/investor might ask.
 *
 * Writes to: src/components/chat/starters.js (overwrites DEFAULT_STARTERS).
 *
 * Requires: ANTHROPIC_API_KEY in env.
 *
 * Usage:
 *   ANTHROPIC_API_KEY=sk-... node scripts/generate-starters.mjs
 */

import fs from 'node:fs/promises'
import path from 'node:path'
import { fileURLToPath } from 'node:url'
import Anthropic from '@anthropic-ai/sdk'

const APP_DIR = path.resolve(fileURLToPath(new URL('..', import.meta.url)))
const INDEX_PATH = path.join(APP_DIR, 'api', '_data', 'research-index.json')
const OUT_PATH = path.join(APP_DIR, 'src', 'components', 'chat', 'starters.js')

const TOPIC_PROMPTS = {
  all: 'all the Clear Current research (deliverables, interviews, secondary)',
  d0: 'the D0 Decision Brief deliverable',
  d1: 'the D1 Engagement Trigger Map deliverable',
  d2: 'the D2 Seasonal Energy Management Calendar deliverable',
  d3: 'the D3 Customer Journey Maps deliverable',
  d4: 'the D4 Product Module Recommendations deliverable',
  d5: 'the D5 Market Opportunity Heatmap deliverable',
  d6: 'the D6 Competitive Intelligence Brief deliverable',
  d7: 'the D7 Investor Narrative & Pitch Framework deliverable',
  d8: 'the D8 GTM Strategy Playbook deliverable',
  interviews: 'the 14 primary founder interviews with energy and facilities leaders',
  secondary: 'the secondary research (market sizing, competitive intel, analyst reports)',
}

function selectChunksForTopic(index, topicId) {
  if (topicId === 'all') {
    // Mix across sources: 1 chunk from each deliverable + 2 interviews + 2 secondary
    const buckets = {}
    for (const ch of index.chunks) {
      const key = `${ch.sourceType}:${ch.sourceId}`
      if (!buckets[key]) buckets[key] = []
      buckets[key].push(ch)
    }
    return Object.values(buckets).flatMap((b) => b.slice(0, 1)).slice(0, 12)
  }
  if (topicId === 'interviews') {
    return index.chunks.filter((c) => c.sourceType === 'interview').slice(0, 12)
  }
  if (topicId === 'secondary') {
    return index.chunks.filter((c) => c.sourceType === 'secondary').slice(0, 12)
  }
  return index.chunks.filter((c) => c.sourceId === topicId)
}

async function main() {
  const apiKey = process.env.ANTHROPIC_API_KEY
  if (!apiKey) {
    console.error('Set ANTHROPIC_API_KEY to run this script.')
    process.exit(1)
  }
  console.log('Loading research index…')
  const index = JSON.parse(await fs.readFile(INDEX_PATH, 'utf8'))
  const client = new Anthropic({ apiKey })

  const result = {}

  for (const [topicId, topicLabel] of Object.entries(TOPIC_PROMPTS)) {
    const chunks = selectChunksForTopic(index, topicId).slice(0, 8)
    const corpus = chunks
      .map((c, i) => `[${i + 1}] ${c.sourceLabel}\n${c.text.slice(0, 800)}`)
      .join('\n\n---\n\n')

    console.log(`Generating starters for ${topicId}…`)
    const resp = await client.messages.create({
      model: 'claude-haiku-4-5',
      max_tokens: 400,
      system: [
        {
          type: 'text',
          text: 'You write sharp, specific starter questions for a research chat assistant. Questions should sound like things a busy founder, investor, or strategy leader would actually ask. Avoid generic "tell me about X" phrasings.',
        },
      ],
      messages: [
        {
          role: 'user',
          content: `Below are excerpts from ${topicLabel}.\n\n${corpus}\n\n---\n\nWrite EXACTLY 3 starter questions a user might click on to begin a conversation focused on this material. Each question must:\n- Be 5–14 words.\n- Be concrete (refer to specific topics, not generic categories).\n- Be answerable from this material.\n- Sound natural, not academic.\n\nReturn ONLY the 3 questions, one per line, no numbering, no preamble.`,
        },
      ],
    })
    const text = resp.content
      .filter((b) => b.type === 'text')
      .map((b) => b.text)
      .join('\n')
      .trim()
    const lines = text
      .split('\n')
      .map((l) => l.replace(/^[-*\d.)\s]+/, '').trim())
      .filter(Boolean)
      .slice(0, 3)
    if (lines.length < 3) {
      console.warn(`  Got only ${lines.length} questions for ${topicId} — keeping defaults.`)
      continue
    }
    result[topicId] = lines
    console.log(`  ✓ ${topicId}`)
  }

  const fileBody = `/**
 * Default starter prompts shown in the topic picker.
 * Generated by scripts/generate-starters.mjs on ${new Date().toISOString()}.
 * Re-run that script to regenerate.
 */

export const DEFAULT_STARTERS = ${JSON.stringify(result, null, 2)}
`
  await fs.writeFile(OUT_PATH, fileBody)
  console.log(`Wrote ${OUT_PATH}`)
}

main().catch((err) => {
  console.error(err)
  process.exit(1)
})
